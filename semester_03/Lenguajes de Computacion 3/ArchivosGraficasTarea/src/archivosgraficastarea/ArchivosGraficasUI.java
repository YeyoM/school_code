    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser open = new JFileChooser();
            int option = open.showOpenDialog(this);
            File f1 = new File(open.getSelectedFile().getPath());
            try (FileReader fr = new FileReader(f1)) {
                BufferedReader br = new BufferedReader(fr);
                String s;
                while((s=br.readLine())!=null) {
                    System.out.println(s);
                    StringTokenizer st = new StringTokenizer(s, "\n");
                    String hombre = (char)34 + "H" + (char)34;
                    String mujer = (char)34 + "M" + (char)34;
                    String estadoABuscar = "10";
                    String salarioABuscar = "888";
                    while(st.hasMoreTokens()) {
                        StringTokenizer st2 = new StringTokenizer(st.nextToken(), ",");
                        while(st2.hasMoreTokens()) {
                            String actual = st2.nextToken();
                            if (actual.equals(hombre)) {
                                sexo = actual;
                                estado = st2.nextToken();
                            } else if (actual.equals(mujer)) {
                                sexo = actual;
                                estado = st2.nextToken();
                            } else {
                                if (sexo.equals(mujer) && estado.equals(estadoABuscar)) {
                                    if (salarioABuscar.equals(actual)) {
                                        cuentaMujeres++;
                                    }
                                }  else if (sexo.equals(hombre) && estado.equals(estadoABuscar)) {
                                    if (salarioABuscar.equals(actual)) {
                                        cuentaHombres++;
                                    }
                                }
                            }
                        }
                    }    
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(ArchivosGraficasUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(ArchivosGraficasUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch(HeadlessException ae) {
            System.out.println(ae);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


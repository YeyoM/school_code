    private CategoryDataset createDataset() {  
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        
        // Estado 10
        dataset.addValue(cuentaMujeres, "Mujeres", "Estado 10");  
        dataset.addValue(cuentaHombres, "Hombres", "Estado 10");  
      
        return dataset;  
    }  
    

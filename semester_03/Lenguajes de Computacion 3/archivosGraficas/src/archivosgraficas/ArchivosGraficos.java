    private void leerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerBtnActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser open = new JFileChooser();
            int option = open.showOpenDialog(this);
            File f1 = new File(open.getSelectedFile().getPath());
            try (FileReader fr = new FileReader(f1)) {
                BufferedReader br = new BufferedReader(fr);
                String s;
                while((s=br.readLine())!=null) {
                    displayDatos.append(s + "\n");
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(ArchivosGraficos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(ArchivosGraficos.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch(HeadlessException ae) {
            System.out.println(ae);
        }
    }//GEN-LAST:event_leerBtnActionPerformed

    private XYDataset createDataset() {  
        XYSeriesCollection dataset = new XYSeriesCollection();  
        
        XYSeries series1 = new XYSeries("Obrero 1"); 
        for (int i = 0; i < 15; i++) {
            series1.add(i + 1, obrero1[i]);
        }
        dataset.addSeries(series1);  
          
       XYSeries series2 = new XYSeries("Obrero 2"); 
        for (int i = 0; i < 15; i++) {
            series2.add(i + 1, obrero2[i]);
        }
        dataset.addSeries(series2);  
        
        XYSeries series3 = new XYSeries("Obrero 3"); 
        for (int i = 0; i < 15; i++) {
            series3.add(i + 1, obrero3[i]);
        }
        dataset.addSeries(series3);  
      
        return dataset;  
    }  
    
    
    private void organizarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_organizarBtnActionPerformed
        // TODO add your handling code here:
        String datos = displayDatos.getText();
        System.out.println(datos);
        StringTokenizer st=new StringTokenizer(datos,"\n"+",");
        int iterador = 1;
        int contadorObreros1 = 0;
        int contadorObreros2 = 0;
        int contadorObreros3 = 0;
        while(st.hasMoreTokens()) {
            if (iterador == 1) {
                int obrero1Data = Integer.parseInt(st.nextToken());
                obrero1[contadorObreros1] = obrero1Data;
                iterador++;
                contadorObreros1++;
            } else if (iterador == 2) {
                obrero2[contadorObreros2] = Integer.parseInt(st.nextToken());
                iterador++;
                contadorObreros2++;
            } else if (iterador == 3) {
                obrero3[contadorObreros3] = Integer.parseInt(st.nextToken());
                iterador = 1;
                contadorObreros3++;
            }
        }
    }//GEN-LAST:event_organizarBtnActionPerformed
  
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        XYDataset dataset = createDataset();  
      
        // Create chart  
        JFreeChart chart = ChartFactory.createScatterPlot(  
            "Obrerosss",   
            "X-Axis", 
            "Y-Axis", 
            dataset
        );
          
        //Changes background color  
        XYPlot plot = (XYPlot)chart.getPlot();  
        plot.setBackgroundPaint(new Color(255,228,196));  
          
         
        // Create Panel  
        ChartPanel panel = new ChartPanel(chart);  
        setContentPane(panel); 
        
        
        panel.setSize(800, 400);  
        panel.setVisible(true);  
    }//GEN-LAST:event_jButton1ActionPerformed


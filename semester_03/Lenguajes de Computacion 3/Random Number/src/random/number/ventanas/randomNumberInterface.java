    private void numberInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_numberInputActionPerformed
        // TODO add your handling code here:
        Random rand = new Random();
        String userInput = numberInput.getText();
        int numberUserInput = parseInt(userInput);
        int randomNum = rand.nextInt((10 - 0) + 1) + 0;
        
        if (numberUserInput == randomNum) {
            resultadoLabel.setText("Felicidades, adivinaste el numero");
            numberInput.setEnabled(false);
        } else if (numberUserInput != randomNum) {
            oportunidades = oportunidades - 1;
            oportunidadesLabel.setText("Oportunidades restantes: " + oportunidades);
            resultadoLabel.setText("Sigue Intentando");
        } 
        
        if (oportunidades == 0) {
            oportunidadesLabel.setText("Oportunidades restantes: 0");
            resultadoLabel.setText("Haz perdido, el n√∫mero era: " + randomNum);
            numberInput.setEnabled(false);
        }
        
    }//GEN-LAST:event_numberInputActionPerformed

